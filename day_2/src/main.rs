use std::collections::HashMap;

fn main() {
    let input = get_input();
    let rounds = input.split("\n");

    let mut total_points = 0;
    for single_round in rounds {
        let competitor_choices = single_round.split(" ").collect::<Vec<&str>>();
        let player_choice = get_response(competitor_choices[0], competitor_choices[1]);
        total_points += get_points(competitor_choices[0], player_choice.as_str());
    }

    println!("Total Points: {}", total_points);
}

fn get_points(input_1: &str, input_2: &str) -> i32 {
    let choice_points = HashMap::from([("A", 1), ("B", 2), ("C", 3)]);
    // println!("{} - {}", input_2, input_1);
    let outcome_points: HashMap<&str, HashMap<&str, i32>> = HashMap::from([
        ("A", HashMap::from([("A", 3), ("B", 0), ("C", 6)])),
        ("B", HashMap::from([("A", 6), ("B", 3), ("C", 0)])),
        ("C", HashMap::from([("A", 0), ("B", 6), ("C", 3)])),
    ]);

    let points = choice_points[input_2] + outcome_points[input_2][input_1];

    return points;
}

fn get_response(opponent_choice: &str, desired_outcome: &str) -> String {
    let choice: HashMap<&str, HashMap<&str, &str>> = HashMap::from([
        ("A", HashMap::from([("X", "C"), ("Y", "A"), ("Z", "B")])),
        ("B", HashMap::from([("X", "A"), ("Y", "B"), ("Z", "C")])),
        ("C", HashMap::from([("X", "B"), ("Y", "C"), ("Z", "A")])),
    ]);
    choice[opponent_choice][desired_outcome].to_string()
}

